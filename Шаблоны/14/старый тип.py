def f(n, d):
    '''
    :param n: натуральное число в десятичной системе счисления
    :param d: основание системы счисления, в которую нужно перевести
    :return: число в виде списка в системе счисления с овнованием d
    '''
    res = []
    while n != 0:
        res.append(n % d)
        n //= d
    return res[::-1]

n = 16**23 + 4**12 - 32**6
n4 = f(n, 4)
for l in sorted(set(n4)):
    print(l, n4.count(l))
